#include "utils.h"

void	ft_free_spl(char **spl)
{
	int	i;
	int	k;

	k = 0;
	i = 0;
	while (spl[i])
		i++;
	while (k < i)
	{
		free(spl[k]);
		k++;
	}
	free(spl);
}
